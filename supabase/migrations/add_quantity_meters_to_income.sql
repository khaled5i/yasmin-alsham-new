-- ============================================================================
-- إضافة حقل الكمية بالمتر إلى جدول الواردات (income)
-- ============================================================================

-- إضافة عمود quantity_meters
ALTER TABLE income
ADD COLUMN IF NOT EXISTS quantity_meters DECIMAL(10, 2) NULL;

-- إضافة تعليق على العمود
COMMENT ON COLUMN income.quantity_meters IS 'الكمية بالمتر (للأقمشة والمواد المباعة بالمتر)';

-- إضافة فهرس للأداء (اختياري - في حال الحاجة للبحث حسب الكمية)
CREATE INDEX IF NOT EXISTS idx_income_quantity_meters ON income(quantity_meters) WHERE quantity_meters IS NOT NULL;

